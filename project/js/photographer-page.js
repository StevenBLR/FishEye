function CleanTitle(title){
    var newTitle;
    allTags.forEach(t => {
        if(title.toString().toUpperCase().includes(t.toString().toUpperCase())){
            console.log(`${title} contains ${t}`);
            newTitle = title.replace(`${t}_`,'');
        }
        else{
            newTitle = title;
        }
    })
    console.log(newTitle);
    return newTitle;
}

